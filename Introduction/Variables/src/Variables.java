public class Variables {
    public static void main(String[] args) {
        System.out.println(("Hello, Andrew"));

        // This is declaration statement we define a variable by
        // including the data type, name, and optionally set a value
        // setting a value = initializing the variable so it can be used
        // once initialized, memory is allocated to store the value
        int myFirstNumber = (5 + 10) + (2 * 10);

        // print out the variable myFirstNumber
        System.out.println("The value of the variable 'myFirstNumber' is: " + myFirstNumber);
    }
}
